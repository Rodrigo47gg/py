nom = str(input('\033[1;31m digite seu nome completo: \033[m')).strip()
print('\033[1;31m seu nome em maiusculas é {}\033[m'.format(nom.upper()))
print('\033[1;31m seu nome em minusculaas é {}\033[m'.format(nom.lower()))
